package com.glad.base;

public class TrdErrorData {

	private String errorCode = null;

	private String errorMessageEN = null;

	private String errorMessageJA = null;

	private String errorFieldName = null;

	public String getErrorCode() {
		return errorCode;
	}

	public void setErrorCode(String errorCode) {
		this.errorCode = errorCode;
	}

	public String getErrorMessageEN() {
		return errorMessageEN;
	}

	public void setErrorMessageEN(String errorMessageEN) {
		this.errorMessageEN = errorMessageEN;
	}

	public String getErrorMessageJA() {
		return errorMessageJA;
	}

	public void setErrorMessageJA(String errorMessageJA) {
		this.errorMessageJA = errorMessageJA;
	}

	public String getErrorFieldName() {
		return errorFieldName;
	}

	public void setErrorFieldName(String errorFieldName) {
		this.errorFieldName = errorFieldName;
	}
}
